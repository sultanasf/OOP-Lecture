    private void bindData() {
        DefaultTableModel tableModel = (DefaultTableModel) cashierTable.getModel();
        tableModel.setRowCount(CashierService.getCashier().getJumlahTransaksi());

        for (int i = 0; i < CashierService.getCashier().getJumlahTransaksi(); i++) {
            CashierService.getCashier().getOneTransaksi(i).setTotalHarga();
            tableModel.setValueAt(CashierService.getCashier().getOneTransaksi(i).getTanggal(), i, 0);
            tableModel.setValueAt(CashierService.getCashier().getOneTransaksi(i).getMobil().getMerkMobil(), i, 1);
            tableModel.setValueAt(CashierService.getCashier().getOneTransaksi(i).getMobil().getPlatNomor(), i, 2);
            tableModel.setValueAt("Rp " + String.valueOf(CashierService.getCashier().getOneTransaksi(i).getTotalHarga()), i, 3);
            tableModel.setValueAt(CashierService.getCashier().getOneTransaksi(i).getStatusSelesai(), i, 4);
            tableModel.setValueAt(CashierService.getCashier().getOneTransaksi(i).getStatusPembayaran(), i, 5);
        }
    }

    private void makeTableListener() {
        cashierTable.getSelectionModel().addListSelectionListener((ListSelectionEvent event) -> {
            boolean isSelected = !cashierTable.getSelectionModel().isSelectionEmpty();
            btnHapus.setEnabled(isSelected);
            btnEdit.setEnabled(isSelected);
            btnDetail.setEnabled(isSelected);
        });
    }

